package com.example.poli_mobile.poli_mobile_entidades;

public class AcercaDe {

    private String dniUniversidad;
    private String presentacion;
    private String historia;
    private String mision;
    private String vision;
    private String escudo;
    private String banderaInstitucion;
    private String himnoInstitucion;

    public String getDniUniversidad() {
        return dniUniversidad;
    }

    public void setDniUniversidad(String dniUniversidad) {
        this.dniUniversidad = dniUniversidad;
    }

    public String getPresentacion() {
        return presentacion;
    }

    public void setPresentacion(String presentacion) {
        this.presentacion = presentacion;
    }

    public String getHistoria() {
        return historia;
    }

    public void setHistoria(String historia) {
        this.historia = historia;
    }

    public String getMision() {
        return mision;
    }

    public void setMision(String mision) {
        this.mision = mision;
    }

    public String getVision() {
        return vision;
    }

    public void setVision(String vision) {
        this.vision = vision;
    }

    public String getEscudo() {
        return escudo;
    }

    public void setEscudo(String escudo) {
        this.escudo = escudo;
    }

    public String getBanderaInstitucion() {
        return banderaInstitucion;
    }

    public void setBanderaInstitucion(String banderaInstitucion) {
        this.banderaInstitucion = banderaInstitucion;
    }

    public String getHimnoInstitucion() {
        return himnoInstitucion;
    }

    public void setHimnoInstitucion(String himnoInstitucion) {
        this.himnoInstitucion = himnoInstitucion;
    }
}