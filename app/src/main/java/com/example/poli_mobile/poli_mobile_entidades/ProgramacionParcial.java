package com.example.poli_mobile.poli_mobile_entidades;

public class ProgramacionParcial {
    String codigoMateria;
    String grupoMateria;
    String diaParcial;
    String horaParcial;
    String aulaParcial;
    String sedeParcial;

    public String getSedeParcial() {
        return sedeParcial;
    }

    public void setSedeParcial(String sedeParcial) {
        this.sedeParcial = sedeParcial;
    }
    
    
    public String getCodigoMateria() {
        return codigoMateria;
    }

    public void setCodigoMateria(String codigoMateria) {
        this.codigoMateria = codigoMateria;
    }

    public String getGrupoMateria() {
        return grupoMateria;
    }

    public void setGrupoMateria(String grupoMateria) {
        this.grupoMateria = grupoMateria;
    }

    public String getDiaParcial() {
        return diaParcial;
    }

    public void setDiaParcial(String diaParcial) {
        this.diaParcial = diaParcial;
    }

    public String getHoraParcial() {
        return horaParcial;
    }

    public void setHoraParcial(String horaParcial) {
        this.horaParcial = horaParcial;
    }

    public String getAulaParcial() {
        return aulaParcial;
    }

    public void setAulaParcial(String aulaParcial) {
        this.aulaParcial = aulaParcial;
    }
    
    
}