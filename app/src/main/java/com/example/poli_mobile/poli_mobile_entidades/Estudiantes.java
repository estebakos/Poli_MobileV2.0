package com.example.poli_mobile.poli_mobile_entidades;


public class Estudiantes{

    private String codestudiante;
    private String nomestudiante;
    private String apeestudiante;
    private String fechaNestudiante;
    private String email;

    public String getCodestudiante() {
        return codestudiante;
    }

    public void setCodestudiante(String codestudiante) {
        this.codestudiante = codestudiante;
    }

    public String getNomestudiante() {
        return nomestudiante;
    }

    public void setNomestudiante(String nomestudiante) {
        this.nomestudiante = nomestudiante;
    }

    public String getApeestudiante() {
        return apeestudiante;
    }

    public void setApeestudiante(String apeestudiante) {
        this.apeestudiante = apeestudiante;
    }

    public String getFechaNestudiante() {
        return fechaNestudiante;
    }

    public void setFechaNestudiante(String fechaNestudiante) {
        this.fechaNestudiante = fechaNestudiante;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

  
}
