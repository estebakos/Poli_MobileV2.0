package com.example.poli_mobile.poli_mobile_entidades;

/**
 *
 * @author brahianstiven
 */
public class AutoevalacionDocente {
    private String codigoMateria;
    private String nombreMateria;
    private String grupoMateria;
    private String notaMateria;
    private String anio;
    private String semestre;

    public String getCodigoMateria() {
        return codigoMateria;
    }

    public void setCodigoMateria(String codigoMateria) {
        this.codigoMateria = codigoMateria;
    }

    public String getNombreMateria() {
        return nombreMateria;
    }

    public void setNombreMateria(String nombreMateria) {
        this.nombreMateria = nombreMateria;
    }

    public String getGrupoMateria() {
        return grupoMateria;
    }

    public void setGrupoMateria(String grupoMateria) {
        this.grupoMateria = grupoMateria;
    }

    public String getNotaMateria() {
        return notaMateria;
    }

    public void setNotaMateria(String notaMateria) {
        this.notaMateria = notaMateria;
    }

    public String getAnio() {
        return anio;
    }

    public void setAnio(String anio) {
        this.anio = anio;
    }

    public String getSemestre() {
        return semestre;
    }

    public void setSemestre(String semestre) {
        this.semestre = semestre;
    }
    
    
}
